<h4><b>Return &amp; Exchanges</b></h4>
<div class="content_col  li_box" data-aos="fade-up">        
<p>At Humnafs, we strive to provide best clothing experience to our customers along with the best possible shopping experience. We understand that sometimes you may need to exchange or return an item, and we want to make this process as easy and convenient as possible.</p>
<p>1.<strong>&nbsp;Exchange Policy:</strong></p>
<ul>
<li>We offer an exchange policy within&nbsp;<strong>15 days&nbsp;</strong>of the date of delivery.</li>
<li>The product(s) must be in their original condition, unused and unwashed, with all tags and labels intact.</li>
<li>We offer a pick-up facility for locations which are serviceable by our logistics partners. Our logistics partner will pick up the return package in 2-5 business days from the date the request was placed.</li>
<li>If the reverse pick-up option is not available at your location, you can self-ship the products to us by any reliable courier service after raising a return request. Please pack the products and ensure that it is unused and unwashed and all the tags are intact. The return courier charges will be reimbursed by us.</li>
<li>The exchange will happen only once.</li>
<li>The exchange must be sent to us within 15 days from the date you received the parcel.</li>
</ul>
